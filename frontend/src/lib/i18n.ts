export type TranslationOptions = {
  fallback?: string;
  params?: Record<string, string | number>;
};

export function getTranslation(lang: Locale, key: string, options?: TranslationOptions): string {
  const parts = key.split(".");
  let current: any = translations[lang];

  for (const part of parts) {
    if (current && typeof current === "object" && part in current) {
      current = current[part];
    } else {
      return options?.fallback ?? key;
    }
  }

  const result = typeof current === "string" ? current : options?.fallback ?? key;

  if (options?.params && typeof result === "string") {
    return Object.entries(options.params).reduce((text, [paramKey, value]) => {
      return text.replace(new RegExp(`\\{${paramKey}\\}`, "g"), String(value));
    }, result);
  }

  return result;
}

export function getAvailableLocales(): Locale[] {
  return Object.keys(translations) as Locale[];
}

export default translations;
